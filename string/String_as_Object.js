//   --->JavaScript String as Objects<---

// Normally, JavaScript string are primitive values, created from literals :

const x = 'Kaushal';

console.log(x);

// ---> But string can also be defined as objects with the Keyword 'new'.

// const x = 'Kaushal';
const y = new string("kaushal");

console.log(x,y);

